package IntroductiontoJava.ThucHanh;

public class KhaiBaoBien {
    public static void main(String[] args) {
        int i = 10; // Khai bao bien i la kieu int co gia tri 10
        float f = 20.5f; // Khai bao bien f co kieu float gia tri 20.5
        double d = 20.5; // khai bao bien d co kieu la double co gia tri la 20.5
        boolean b = true; // khai bao bien b co kieu la boolean co gia tri la true
        char c = 'a'; // khai bao bien c co kieu la ky tu cos gia tri la a
        String s = "Ha Noi"; // khai bao bien co kieu la chuoi co gia tri la Ha Noi
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
